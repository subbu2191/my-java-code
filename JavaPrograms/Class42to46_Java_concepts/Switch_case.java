package Class42to46_Java_concepts;

public class Switch_case 
{
public static void main(String[] args) 
{
switch(5)
{
	case 1: System.out.println("Chrome Browser");      //switch(4)-->4 means case number, 4 alone execute
	break;
	case 2: System.out.println("Edge Browser");
	break;
	case 3: System.out.println("Safari Browser");
	break;
	case 4: System.out.println("Firefox Browser");
	break;
	
	default: System.out.println("default message: Sorry your selection is wrong. There is no the selected option");
		
}
	
	
	
	
}
}
